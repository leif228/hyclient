package com.hangyi.zd.domain;

public enum ShipModelCode {
	// 模块类型：0GPS，1流量计，2转速，3液位计，4设备状态，5货舱画面
	zero(0, "GPS"), one(1, "流量计"), two(2, "转速"), three(3, "液位计"), four(4, "设备状态"), five(5, "货舱画面");
	private int n;
	private String description;

	private ShipModelCode(int n, String description) {
		this.n = n;
		this.description = description;
	}

	public String getDescription() {
		return description;
	}
	
	public int getN() {
		return n;
	}
}
