package com.hangyi.zd.domain;

import java.util.ArrayList;
import java.util.List;

import com.eyunda.third.domain.BaseData;

public class ShipModelData extends BaseData {
	private static final long serialVersionUID = -1L;
	private String shipID = "";
	private String shipName = "";
	private ShipModelCode model = ShipModelCode.zero;

	private List<ShipModelNoData> modelNolist = new ArrayList<ShipModelNoData>();// 模块下设备编号

	public ShipModelCode getModel() {
		return model;
	}

	public void setModel(ShipModelCode model) {
		this.model = model;
	}

	public String getShipName() {
		return shipName;
	}

	public void setShipName(String shipName) {
		this.shipName = shipName;
	}

	public String getShipID() {
		return shipID;
	}

	public void setShipID(String shipID) {
		this.shipID = shipID;
	}

	public List<ShipModelNoData> getModelNolist() {
		return modelNolist;
	}

	public void setModelNolist(List<ShipModelNoData> modelNolist) {
		this.modelNolist = modelNolist;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

}
